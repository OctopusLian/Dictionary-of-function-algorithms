#include<stdio.h>
#define N 20
void main()
{
	int x0[N+1],x1[N+1],x2[N+1],x3[N+1],i,s;
	/*初始时，只有一头刚出生的母牛*/
	x0[0]=1;
	x1[0]=x2[0]=x3[0]=0;
	for(i=1;i<=N;i++)
	{
		x0[i]=x3[i]=x2[i-1]+x3[i-1];/*满2岁和满3岁的母牛成为育龄牛，且都生了小母牛*/
		x1[i]=x0[i-1];/*刚生下的小母牛成为下一年的满1岁母牛*/
		x2[i]=x1[i-1];/*满1岁的小母牛成为下一年的满2岁母牛*/
		s=x0[i]+x1[i]+x2[i]+x3[i];/*第i年的母牛总数*/
		printf("第%d年后母牛的总数:%4d\n",i,s);
	}
}